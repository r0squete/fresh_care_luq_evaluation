def load_luq_csv_results(results_dir):
    """
    Load LUQ results from CSV files generated by run_evaluation.py.
    Files use semicolon (;) as separator.

    Inputs
    ------
    results_dir : str or Path

    Returns
    -------
    dict  {drifter_id: {'summary': df, 'timeseries': df, 'metadata': dict}}
    """
    results_path = Path(results_dir)
    csv_files = list(results_path.glob("luq_results_drifter_*.csv"))
    if not csv_files:
        print(f"No LUQ result CSV files found in {results_dir}")
        return {}

    drifters_data = {}
    for csv_file in csv_files:
        filename = csv_file.name
        try:
            parts = filename.split("_")
            drifter_idx = int(parts[3])
        except (IndexError, ValueError):
            print(f"Could not parse drifter index from {filename}")
            continue

        df = pd.read_csv(csv_file, sep=";")
        if df.empty:
            continue

        summary_df = df[df["data_type"] == "summary"].copy()
        timeseries_df = df[df["data_type"] == "timeseries"].copy()
        if not timeseries_df.empty:
            timeseries_df["t0_datetime"] = pd.to_datetime(timeseries_df["t0_datetime"])

        metadata = {
            "filename": summary_df["drifter_filename"].iloc[0]
            if not summary_df.empty
            else f"drifter_{drifter_idx}",
            "L_characteristic_km": summary_df["L_characteristic_km"].iloc[0]
            if not summary_df.empty
            else np.nan,
        }
        drifters_data[drifter_idx] = {
            "summary": summary_df,
            "timeseries": timeseries_df,
            "metadata": metadata,
        }

    print(
        f"Loaded results for {len(drifters_data)} drifters from {len(csv_files)} CSV files"
    )
    return drifters_data


def plot_luq_time_series_from_csv(
    drifters_data,
    drifter_id=None,
    dataset_names=None,
    tau_days=5,
    r_km=10,
    save_path=None,
):
    """
    Plot LUQ normalized time series from CSV results.

    Inputs
    ------
    drifters_data  : dict, output of load_luq_csv_results
    drifter_id     : int, drifter to plot (default: first available)
    dataset_names  : list of str, datasets to plot (default: all)
    tau_days       : int, integration time in days
    r_km           : int, neighborhood radius in km
    save_path      : str, path to save figure

    Returns
    -------
    fig, ax
    """
    if drifter_id is None:
        drifter_id = list(drifters_data.keys())[0]
    if drifter_id not in drifters_data:
        raise ValueError(f"Drifter {drifter_id} not found in data")

    timeseries_df = drifters_data[drifter_id]["timeseries"]
    filtered_df = timeseries_df[
        (timeseries_df["tau_days"] == tau_days) & (timeseries_df["r_km"] == r_km)
    ].copy()

    if filtered_df.empty:
        print(f"No data found for drifter {drifter_id}, tau={tau_days}d, r={r_km}km")
        return None, None

    available_datasets = filtered_df["dataset"].unique()
    if dataset_names is None:
        dataset_names = available_datasets
    else:
        dataset_names = [d for d in dataset_names if d in available_datasets]

    if len(dataset_names) == 0:
        print("No valid datasets found")
        return None, None

    fig, ax = plt.subplots(figsize=(10, 6))
    colors = {
        "ADT-SST": "purple",
        "ADT-SSS": "blue",
        "ADT-0.05": "olive",
        "ADT-0.25": "orange",
        "OSCAR-total": "red",
        "OSCAR-geos": "green",
    }

    for dataset_name in dataset_names:
        dataset_df = filtered_df[filtered_df["dataset"] == dataset_name].sort_values(
            "t0_datetime"
        )
        if dataset_df.empty:
            continue
        ax.plot(
            dataset_df["t0_datetime"],
            dataset_df["luq_normalized"],
            color=colors.get(dataset_name, "black"),
            label=dataset_name,
            linewidth=2.0,
        )
        n_total = len(dataset_df)
        n_valid = int(dataset_df["luq_normalized"].notna().sum())
        print(f"  {dataset_name}: {n_valid}/{n_total} valid points")

    ax.set_xlabel("t₀", fontsize=14)
    ax.set_ylabel("LUQ normalized mean", fontsize=14)
    ax.legend(fontsize=12, loc="upper right")
    ax.grid(True, alpha=0.3)
    ax.tick_params(axis="x", rotation=45, labelsize=11)
    ax.tick_params(axis="y", labelsize=11)
    plt.tight_layout()

    if save_path:
        fig.savefig(save_path, dpi=300, bbox_inches="tight")
        print(f"Plot saved: {save_path}")

    return fig, ax


def _load_field_cropped(name, t0_dt, tau_sec, lon0_deg, lat0_deg, margin):
    """
    Load a dataset cropped in time (t0 → t0+tau) and space (±margin degrees).

    Inputs
    ------
    name        : str, dataset name in DATASETS config
    t0_dt       : pd.Timestamp
    tau_sec     : int64, integration window in seconds
    lon0_deg    : float, center longitude
    lat0_deg    : float, center latitude
    margin      : float, spatial margin in degrees

    Returns
    -------
    field : dict from prep_dataset
    """
    t_start = t0_dt.strftime("%Y-%m-%d")
    # +1 day buffer: _find_index_uniform needs i < n-1, the field must have
    # one timestep beyond t0+τ for the final RK4 k4 evaluation.
    t_end = (
        t0_dt + pd.Timedelta(seconds=int(tau_sec)) + pd.Timedelta(days=1)
    ).strftime("%Y-%m-%d")
    ds = load_dataset_files(
        DATASETS[name]["path"],
        eval_start=t_start,
        eval_end=t_end,
    )
    lon_name = DATASETS[name]["lon_name"]
    lat_name = DATASETS[name]["lat_name"]
    lon_vals = ds[lon_name].values
    lat_vals = ds[lat_name].values
    # Normalize lon0 to match the dataset longitude convention ([0,360] or [-180,180]).
    # For [0,360] datasets whose crop window crosses 0° (low < 0), a two-sided mask
    # is required: [0, high] ∪ [360+low, 360]. prep_dataset will then wrap and sort
    # the resulting longitudes into [-180,180] correctly.
    lon0_crop = lon0_deg
    if lon_vals.min() >= 0 and lon0_deg < 0:
        lon0_crop = lon0_deg + 360
    elif lon_vals.max() <= 180 and lon0_deg > 180:
        lon0_crop = lon0_deg - 360
    low = lon0_crop - margin
    high = lon0_crop + margin
    if lon_vals.min() >= 0 and low < 0:
        # Crop window crosses 0° in a [0,360] dataset — select both sides.
        lon_mask = (lon_vals <= high) | (lon_vals >= 360 + low)
    else:
        lon_mask = (lon_vals >= low) & (lon_vals <= high)
    lat_mask = (lat_vals >= lat0_deg - margin) & (lat_vals <= lat0_deg + margin)
    lon_dim = ds[lon_name].dims[0]
    lat_dim = ds[lat_name].dims[0]
    ds = ds.isel({lon_dim: np.where(lon_mask)[0], lat_dim: np.where(lat_mask)[0]})
    field = prep_dataset(
        ds,
        u_name=DATASETS[name]["u_name"],
        v_name=DATASETS[name]["v_name"],
        time_name=DATASETS[name]["time_name"],
        lat_name=lat_name,
        lon_name=lon_name,
    )
    ds.close()
    return field


def main():
    parser = argparse.ArgumentParser(
        description="Generate LUQ analysis plots from CSV results",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--results_dir",
        type=str,
        default="../results/",
        help="Directory with LUQ CSV results",
    )
    parser.add_argument(
        "--drifter_id",
        type=int,
        default=argparse.SUPPRESS,
        help="Drifter ID to plot (default: first available)",
    )
    parser.add_argument(
        "--datasets",
        nargs="+",
        default=argparse.SUPPRESS,
        help="Datasets to plot (default: all available)",
    )
    parser.add_argument(
        "--tau_days",
        type=int,
        default=10,
        help="Integration time in days (used for all plots)",
    )
    parser.add_argument(
        "--r_km",
        type=int,
        default=15,
        help="Neighborhood radius in km (used for all plots)",
    )
    parser.add_argument(
        "--save_plots", action="store_true", help="Save plots after showing them"
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default="../results/plots/",
        help="Directory to save plots",
    )
    # Time series plot args
    parser.add_argument(
        "--timeseries",
        action="store_true",
        help="Generate LUQ normalized time series plot",
    )
    # LUQ map args
    parser.add_argument("--map", action="store_true", help="Generate LUQ spatial map")
    parser.add_argument(
        "--t0",
        type=str,
        default=argparse.SUPPRESS,
        help=(
            "Snapshot time for map plots ('YYYY-MM-DD HH:MM:SS'). "
            "Must be used together with --drifter_id."
        ),
    )
    parser.add_argument(
        "--map_datasets",
        nargs="+",
        default=argparse.SUPPRESS,
        help="Datasets for LUQ map (default: all available)",
    )
    # M map args
    parser.add_argument(
        "--M", action="store_true", help="Generate Lagrangian descriptor M map"
    )
    parser.add_argument(
        "--M_nx", type=int, default=500, help="Grid resolution (lon) for M map"
    )
    parser.add_argument(
        "--M_ny", type=int, default=500, help="Grid resolution (lat) for M map"
    )

    args = parser.parse_args()

    print("=== LUQ Graph Analysis ===")
    print(f"Loading data from: {args.results_dir}")
    drifters_data = load_luq_csv_results(args.results_dir)
    if not drifters_data:
        print("No data loaded. Exiting.")
        return

    if args.save_plots:
        Path(args.output_dir).mkdir(exist_ok=True)

    drifter_id = getattr(args, "drifter_id", None)
    dataset_names = getattr(args, "datasets", None)

    # Early validation: check that the requested tau/r combination exists
    check_id = drifter_id or list(drifters_data.keys())[0]
    ts_check = drifters_data[check_id]["timeseries"]
    ts_check = ts_check[
        (ts_check["tau_days"] == args.tau_days) & (ts_check["r_km"] == args.r_km)
    ]
    if ts_check.empty:
        available_tau = sorted(
            drifters_data[check_id]["timeseries"]["tau_days"].unique()
        )
        available_r = sorted(drifters_data[check_id]["timeseries"]["r_km"].unique())
        print(
            f"  ✗ tau={args.tau_days}d / r={args.r_km}km not found in results "
            f"for drifter {check_id}.\n"
            f"    Available tau_days: {available_tau}\n"
            f"    Available r_km:     {available_r}"
        )
        return

    # --- Time series plot ---
    if args.timeseries:
        print("\nCreating time series plot...")
        fig, ax = plot_luq_time_series_from_csv(
            drifters_data,
            drifter_id=drifter_id,
            dataset_names=dataset_names,
            tau_days=args.tau_days,
            r_km=args.r_km,
            save_path=f"{args.output_dir}timeseries_drifter_{drifter_id or 'auto'}_tau{args.tau_days}_r{args.r_km}.png"
            if args.save_plots
            else None,
        )
        if fig:
            # Use 'best' location for legend to avoid covering lines
            ax.legend(fontsize=12, loc="best")
            plt.show()
            plt.close(fig)

    # --- LUQ map / M map ---
    if args.map or args.M:
        # Shared setup: resolve reference t0, lon0, lat0 for the map seed point.
        t0 = getattr(args, "t0", None)
        t0_dt = pd.to_datetime(t0) if t0 else None
        t0_tag = t0.replace(" ", "_").replace(":", "") if t0 else "auto"
        drifter_id_map = drifter_id or list(drifters_data.keys())[0]
        # Filter timeseries to the tau and r_km used for the maps.
        ts_df = drifters_data[drifter_id_map]["timeseries"]
        ts_df = ts_df[
            (ts_df["tau_days"] == args.tau_days) & (ts_df["r_km"] == args.r_km)
        ]

        if t0_dt is not None:
            ref_row = ts_df[ts_df["t0_datetime"] == t0_dt]
            if ref_row.empty:
                ts_df2 = ts_df.copy()
                ts_df2["_dt"] = (ts_df2["t0_datetime"] - t0_dt).abs()
                ref_row = ts_df2.loc[[ts_df2["_dt"].idxmin()]]
                print(
                    f"  t0 not found exactly, using nearest: {ref_row['t0_datetime'].iloc[0]}"
                )
        else:
            # lon0_deg/lat0_deg are always real (from GPS, not LUQ),
            # but drop on them to be explicit about what we need.
            ref_row = ts_df.dropna(subset=["lon0_deg", "lat0_deg"]).iloc[[0]]
        # t0_ref is the actual reference time drawn from the data row (may differ from
        # the user-supplied --t0 if none was passed or the nearest match was used).
        t0_ref = pd.Timestamp(ref_row["t0_datetime"].iloc[0])
        lon0_deg = ref_row["lon0_deg"].iloc[0]
        lat0_deg = ref_row["lat0_deg"].iloc[0]
        margin = 2.0  # degrees — seed grid domain, domain in M graph!!!!!!
        margin_crop = 40.0  # degrees — field crop domain
        tau_sec = np.int64(args.tau_days * 24 * 3600)
        t0_sec = to_epoch_seconds_datetime64(
            np.array([t0_ref.to_datetime64()], dtype="datetime64[s]")
        )[0]
        map_dataset_names = getattr(args, "map_datasets", None) or list(DATASETS.keys())

        # grids accumulated across datasets for shared color scale
        luq_grids = {}
        M_grids = {}
        all_luq_max = []
        all_M_max = []

        # M spatial grid (same domain for all datasets)
        if args.M:
            lon_grid = np.linspace(
                np.deg2rad(lon0_deg - margin),
                np.deg2rad(lon0_deg + margin),
                args.M_nx,
                dtype=np.float32,
            )
            lat_grid = np.linspace(
                np.deg2rad(lat0_deg - margin),
                np.deg2rad(lat0_deg + margin),
                args.M_ny,
                dtype=np.float32,
            )

        # Load GPS drifter target
        lon0_rad = np.float32(np.deg2rad(lon0_deg))
        lat0_rad = np.float32(np.deg2rad(lat0_deg))
        lon_target = lat_target = None  # set below if --map requested
        if args.map:
            t_target_sec = t0_sec + tau_sec
            drifter_filename = drifters_data[drifter_id_map]["metadata"]["filename"]
            drifter_file_path = Path(DRIFTERS_PATH) / drifter_filename
            try:
                df_drift = load_drifter_file(drifter_file_path)
                drifter = prep_drifter(
                    df_drift,
                    time_col=DRIFTER_COLUMNS["time_col"],
                    lon_col=DRIFTER_COLUMNS["lon_col"],
                    lat_col=DRIFTER_COLUMNS["lat_col"],
                )
                idx = int(np.argmin(np.abs(drifter["time_sec"] - t_target_sec)))
                dt_gap = abs(int(drifter["time_sec"][idx]) - int(t_target_sec))
                max_gap_sec = 3600  # hourly CSV: nearest obs must be ≤ 1 h away
                if dt_gap > max_gap_sec:
                    print(
                        f"  ⚠ Drifter GPS target at t0+τ is {dt_gap / 3600:.1f} h away "
                        f"(>{max_gap_sec / 3600:.0f} h) — LUQ maps skipped (data gap?)"
                    )
                else:
                    lon_target = drifter["lon_rad"][idx]
                    lat_target = drifter["lat_rad"][idx]
                    print(
                        f"  GPS target at t0+τ: "
                        f"lon={np.rad2deg(lon_target):.3f}°  "
                        f"lat={np.rad2deg(lat_target):.3f}°  "
                        f"(Δt={dt_gap / 60:.0f} min)"
                    )
            except Exception as e_drift:
                print(f"  ⚠ Could not load drifter GPS target — {e_drift}")

        for name in map_dataset_names:
            if name not in DATASETS:
                continue
            try:
                field = _load_field_cropped(
                    name, t0_ref, tau_sec, lon0_deg, lat0_deg, margin_crop
                )

                if args.map:
                    if lon_target is None:
                        print(f"  ⚠ {name}: no valid GPS target — LUQ map skipped")
                    else:
                        (
                            luq_mean_km,
                            valid_frac_ret,
                            seed_lon_grid,
                            seed_lat_grid,
                            LUQ_grid_km,
                            valid_grid,
                        ) = luq_case(
                            field,
                            t0_sec,
                            tau_sec,
                            DATASETS[name].get("dt_sec", 3600),
                            lon0_rad,
                            lat0_rad,
                            lon_target,
                            lat_target,
                            args.r_km,
                            return_map=True,
                        )
                        n_valid = int(valid_grid.sum())
                        n_total = valid_grid.size
                        n_nan = int(np.isnan(LUQ_grid_km).sum())
                        print(
                            f"  [{name}] luq_case → luq_mean={luq_mean_km:.3f} km  "
                            f"valid={n_valid}/{n_total} ({valid_frac_ret:.2%})  "
                            f"NaN in grid={n_nan}/{n_total}"
                        )
                        luq_grids[name] = {
                            "lon_deg": np.rad2deg(seed_lon_grid),
                            "lat_deg": np.rad2deg(seed_lat_grid),
                            "luq_km": LUQ_grid_km,
                        }
                        if not np.all(np.isnan(LUQ_grid_km)):
                            all_luq_max.append(np.nanmax(LUQ_grid_km))

                if args.M:
                    M_grid = compute_M(
                        field,
                        t0_sec,
                        tau_sec,
                        DATASETS[name].get("dt_sec", 3600),
                        lon_grid,
                        lat_grid,
                    )
                    M_grids[name] = M_grid
                    if not np.all(np.isnan(M_grid)):
                        all_M_max.append(np.nanmax(M_grid))

                del field
            except Exception as e:
                print(f"  ✗ {name}: {e}")

        # --- Plot LUQ map ---
        if args.map:
            if not luq_grids:
                print("No LUQ grids computed.")
            else:
                vmax = max(all_luq_max) if all_luq_max else None
                for name, g in luq_grids.items():
                    lon_center = g["lon_deg"].mean()
                    lon_label = f"Longitude (°{'W' if lon_center < 0 else 'E'})"
                    fig, ax = plt.subplots(figsize=(6, 5))
                    pcm = ax.pcolormesh(
                        g["lon_deg"],
                        g["lat_deg"],
                        g["luq_km"],
                        cmap="YlOrRd",
                        vmin=0,
                        vmax=vmax,
                        shading="auto",
                    )
                    ax.plot(lon0_deg, lat0_deg, "co", markersize=5)
                    ax.set_xlabel(lon_label, fontsize=12)
                    ax.set_ylabel("Latitude (°N)", fontsize=12)
                    ax.tick_params(labelsize=10)
                    ax.xaxis.set_major_formatter(plt.FormatStrFormatter("%.2f"))
                    ax.yaxis.set_major_formatter(plt.FormatStrFormatter("%.2f"))
                    ax.tick_params(axis="x", rotation=45)
                    plt.colorbar(pcm, ax=ax, label="LUQ (km)")
                    fig.suptitle(name, fontsize=13)
                    plt.tight_layout()
                    plt.show()
                    if args.save_plots:
                        out = f"{args.output_dir}map_luq_{drifter_id_map}_t0{t0_tag}_tau{args.tau_days}_r{args.r_km}_{name}.png"
                        fig.savefig(out, dpi=300, bbox_inches="tight")
                        print(f"  Saved: {out}")
                    plt.close(fig)

        # --- Plot M map ---
        if args.M:
            if not M_grids:
                print("No M grids computed.")
            else:
                vmax_M = max(all_M_max) if all_M_max else None
                lon_deg_grid = np.rad2deg(lon_grid)
                lat_deg_grid = np.rad2deg(lat_grid)
                lon_mesh, lat_mesh = np.meshgrid(lon_deg_grid, lat_deg_grid)
                lon_label_M = f"Longitude (°{'W' if lon0_deg < 0 else 'E'})"
                r_km_deg = args.r_km / 111.0
                for name, M_grid in M_grids.items():
                    rect = Rectangle(
                        (lon0_deg - r_km_deg, lat0_deg - r_km_deg),
                        2 * r_km_deg,
                        2 * r_km_deg,
                        linewidth=1.5,
                        edgecolor="red",
                        facecolor="none",
                    )
                    fig, ax = plt.subplots(figsize=(6, 5))
                    pcm = ax.pcolormesh(
                        lon_mesh,
                        lat_mesh,
                        M_grid,
                        cmap="binary_r",
                        vmin=0,
                        vmax=vmax_M,
                        shading="auto",
                    )
                    ax.plot(lon0_deg, lat0_deg, "co", markersize=5)
                    ax.add_patch(rect)
                    ax.set_xlabel(lon_label_M, fontsize=12)
                    ax.set_ylabel("Latitude (°N)", fontsize=12)
                    ax.tick_params(labelsize=10)
                    ax.xaxis.set_major_formatter(plt.FormatStrFormatter("%.2f"))
                    ax.yaxis.set_major_formatter(plt.FormatStrFormatter("%.2f"))
                    ax.tick_params(axis="x", rotation=45)
                    plt.colorbar(pcm, ax=ax, label="M")
                    fig.suptitle(name, fontsize=13)
                    plt.tight_layout()
                    plt.show()
                    if args.save_plots:
                        out = f"{args.output_dir}map_M_{drifter_id_map}_t0{t0_tag}_tau{args.tau_days}_r{args.r_km}_{name}.png"
                        fig.savefig(out, dpi=300, bbox_inches="tight")
                        print(f"  Saved: {out}")
                    plt.close(fig)

    if not (args.timeseries or args.map or args.M):
        print("\nSelect plots to generate.")


if __name__ == "__main__":
    main()
